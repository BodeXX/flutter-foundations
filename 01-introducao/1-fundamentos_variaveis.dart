// Variáveis e TIpos de Dados

void main () {
    // A variável 'name' armazena uma string (texto)
    String name = 'Eduardo Mendes';

    // A variável 'age' armazena um número inteiro
    int age = 52;

    // A variável 'height' armazena um número decimal (altura)
    double height = 1.86;

    // A variável 'isStudent' armazena um valor boolenado (verdadeiro ou falso)
    bool isStudent = true;


    // Exibir as informações no console
    print('Name: $name'); // Exibe valor da variável 'name'
    print('Age: $age'); // Exibe valor da variável 'age'
    print('Height: $height'); // Exibe valor da variável 'height'
    print('isStudent: $isStudent'); // Exibe valor da variável 'isStudent'
}
